        /// Информация о том, разрешено ли написание личных сообщений данному пользователю.
        /// </summary>
        can_write_private_message,
        /// <summary>
        /// Статус пользователя.
        /// </summary>
        status,
        /// <summary>
        /// Время последнего посещения пользователя.
        /// </summary>
        last_seen,
        /// <summary>
        /// Количество подписчиков пользователя.
        /// </summary>
        followers_count,
        /// <summary>
        /// Количество общих друзей с текущим пользователем.
        /// </summary>
        common_count,
        /// <summary>
        /// Количество различных объектов у пользователя.
        /// </summary>
        counters,
        /// <summary>
        /// Информация о текущем роде занятия пользователя.
        /// </summary>        
        occupation,
        /// <summary>
        /// Никнейм (отчество) пользователя.
        /// </summary>
        nickname,
        /// <summary>
        /// Семейное положение пользователя.
        /// </summary>
        relation,
        /// <summary>
        /// Список родственников текущего пользователя.
        /// </summary>
        relatives,
        /// <summary>
        /// Информация о полях из раздела "Жизненная позиция".
        /// </summary>
        personal,
        /// <summary>
        /// Внешние сервисы, в который настроен импорт из ВКонтакте.
        /// </summary>
        exports,
        /// <summary>
        /// Короткий адрес страницы пользователя.
        /// </summary>
        screen_name,
        /// <summary>
        /// Временная зона пользователя (только текущего).
        /// </summary>
        timezone
