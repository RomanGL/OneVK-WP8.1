    /// </summary>
    public enum VKUserFields
    {
        /// <summary>
        /// Указывает на верификацию страницы пользователя.
        /// </summary>
        verified,
        /// <summary>
        /// Пол пользователя.
        /// </summary>
        sex,
        /// <summary>
        /// Дата рождения пользователя.
        /// </summary>
        bdate,
        /// <summary>
        /// Указывает на наличие пользователя в черном списке у запрашиваемого.
        /// </summary>
        blacklisted,
        /// <summary>
        /// Идентификатор города, указанного на странице пользователя.
        /// </summary>
        city,
        /// <summary>
        /// Идентификатор страны, указанной на странице пользователя.
        /// </summary>
        country,
        /// <summary>
        /// Ссылка на квадратную фотографию пользователя размером 50px.
        /// </summary>
        photo_50,
        /// <summary>
        /// Ссылка на квадратную фотографию пользователя размером 100px.
        /// </summary>
        photo_100,
        /// <summary>
        /// Ссылка на фотографию пользователя шириной 200px.
        /// </summary>
        photo_200_orig,
        /// <summary>
        /// Ссылка на квадратную фотографию пользователя размером 200px.
        /// </summary>
        photo_200,
        /// <summary>
        /// Ссылка на фотографию пользователя шириной 400px.
        /// </summary>
        photo_400_orig,
        /// <summary>
        /// Ссылка на квадратную фотографию пользователя с максимальной шириной.
        /// </summary>
        photo_max,
        /// <summary>
        /// Ссылка на фотографию пользователя максимального размера.
        /// </summary>
        photo_max_orig,
        /// <summary>
        /// Информация о том, находится ли пользователь сейчас в сети.
        /// </summary>
        online,
        /// <summary>
        /// Идентификаторы списков друзей, в которых состоит пользователь.
        /// </summary>
        domain,
        /// <summary>
        /// Информация о том, известен ли номер мобильного телефона пользователя.
        /// </summary>
        has_mobile,
        /// <summary>
        /// Информация о телефонных номерах пользователя.
        /// </summary>
        contacts,
        /// <summary>
        /// Информация у других контактах пользователя (например, Skype).
        /// </summary>
        connections,
        /// <summary>
        /// Сайт, указанный в профиле пользователя.
        /// </summary>
        site,
        /// <summary>
        /// Информация о высшем учебном заведении пользователя.
        /// </summary>
        education,
        /// <summary>
        /// Список высших учебных заведений, в которых учился пользователь.
        /// </summary>
        universities,
        /// <summary>
        /// Список школ, в которых учился пользователь.
        /// </summary>
        schools,
        /// <summary>
        /// Доступно ли комментирование стены.
        /// </summary>
        wall_comments,
        /// <summary>
        /// Деятельность пользователя.
        /// </summary>
        activities,
        /// <summary>
        /// Интересы пользователя.
        /// </summary>
        interests,
        /// <summary>
        /// Любимая музыка пользователя.
        /// </summary>
        music,
        /// <summary>
        /// Любимые фильмы пользователя.
        /// </summary>
        movies,
        /// <summary>
        /// Любимые телешоу пользователя.
        /// </summary>
        tv,
        /// <summary>
        /// Любимые книги пользователя.
        /// </summary>
        books,
        /// <summary>
        /// Любимые игры пользователя.
        /// </summary>
        games,
        /// <summary>
        /// Раздел "О себе".
        /// </summary>
        about,
